package com.javacode_4thMarch_2023;

public class Concatenation_In_Java {

	public static void main(String[] args) {
		int a = 10;
		double d = 21.34;
		
		String x = "Hello";
		
		String y = "World";
		
		System.out.println(a+d+x+y);
		
		System.out.println(x+y+a+d);
		
		System.out.println(x+y+(a+d));

	}

}
