	// constructor is a block of code which looks like a method but it is not a
	// method.

	public ConstructorConcepts() {

	}

	// method vs constructors
	// 1. constructors do not have a return type but methods have a return type
	// 2. the name of the constructor always is the class name but methods can
	// have any name.
	// 3. constructors get invoked the moment an object is created. that is not the
	// case with methods.
	// 4. methods can be inherited but constructors cannot be inherited
	// 5. constructors cannot be made static but methods can be made static

	// private, public, default, protected - can be used with constructors

	// uses of constructor:-
	// 1. constructors are used to initialize an object
	// 2. they are used to optimize code

	// types of constructors
	// 1. default constructor (this gets invoked the moment an object is created)
	// 2. no argument constructor (this is user defined)
	// 3. parameterized constructor (this is also user defined)

	// misconception about constructor: 
	// constructors are used to create objects (please do not answer/agree to this
	// in interview).

